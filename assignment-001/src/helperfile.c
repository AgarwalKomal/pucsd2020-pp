float AddNums(float iNum1, float iNum2)
{
  return (iNum1 + iNum2);
}

float SubNums(float iNum1, float iNum2)
{
  return (iNum1 - iNum2);
}

float MultNums(float iNum1, float iNum2)
{
  return (iNum1 * iNum2);
}

float DivNums(float iNum1, float iNum2)
{
  return (iNum1 / (float)iNum2);
}
